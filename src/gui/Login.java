    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jTextField1.getText().equals("")||jPasswordField1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Porfavor escriba un nombre y/o contrase単a");
        }else{
          try {
            usuario = usuarioDAO.selectedUsuario(jTextField1.getText());
         
            if (usuario.getNombre().equals(jTextField1.getText())&&usuario.getContrasena().equals(jPasswordField1.getText())) {
            JOptionPane.showMessageDialog(null, "Acceso concedido");
            inicio.setVisible(true);
            this.dispose();
            }else{
            JOptionPane.showMessageDialog(null, "El usuario o contrase単a son incorrectos");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "El usuario o contrase単a son incorrectos");
              System.out.println("Error "+e);
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

